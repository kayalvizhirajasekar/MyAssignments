package week1.day2;

import java.util.Iterator;

public class ForLoop {

	public static void main(String[] args) {
		//for loop
		for (int i = 0; i< 5 ; i++) 
		{
			System.out.println(i);
		}
		
		//Reverse for loop
		for(int i = 5 ; i >=0 ; i--)
		{
			System.out.println(i);
		}
	}
	}